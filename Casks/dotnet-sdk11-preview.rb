  arch arm: "arm64", intel: "x64"

  version "11.0.100-preview.5.26302.115,11.0.0-preview.5.26302.115"

  sha256_x64 = "e8f712518e970547b71b939a986030410c48f5d2537be94a0d04cc8c110b2891"
